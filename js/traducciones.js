// Obtener idioma guardado o usar español por defecto
function getIdiomaActual() {
  return localStorage.getItem("idioma") || "es";
}

// Guardar idioma
function setIdioma(idioma) {
  localStorage.setItem("idioma", idioma);
}

// Traducir un texto
function t(clave) {
  const idioma = getIdiomaActual();
  return traducciones[idioma][clave] || traducciones["es"][clave] || clave;
}

// Aplicar traducciones a toda la página
function aplicarTraducciones() {
  const idioma = getIdiomaActual();

  // Actualizar todos los elementos con data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const clave = el.getAttribute("data-i18n");
    const traduccion = t(clave);

    // Si tiene HTML (como <strong>), usar innerHTML
    if (traduccion.includes("<")) {
      el.innerHTML = traduccion;
    } else {
      el.textContent = traduccion;
    }
  });

  // Actualizar placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const clave = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(clave);
  });

  // Actualizar el botón de idioma con colores de bandera
  const botonIdioma = document.getElementById("boton-idioma");
  if (botonIdioma) {
    if (idioma === "es") {
      // Está en español, mostrar EN con bandera UK
      botonIdioma.textContent = "EN";
      botonIdioma.title = "Switch to English";
      botonIdioma.classList.remove("bandera-es");
      botonIdioma.classList.add("bandera-uk");
    } else {
      // Está en inglés, mostrar ES con bandera España
      botonIdioma.textContent = "ES";
      botonIdioma.title = "Switch to Spanish";
      botonIdioma.classList.remove("bandera-uk");
      botonIdioma.classList.add("bandera-es");
    }
  }

  // Actualizar atributo lang del HTML
  document.documentElement.lang = idioma;

  // Disparar evento para que otros scripts sepan que cambió el idioma
  window.dispatchEvent(
    new CustomEvent("idiomaChanged", { detail: { idioma } })
  );
}

// Cambiar idioma
function cambiarIdioma() {
  const idiomaActual = getIdiomaActual();
  const nuevoIdioma = idiomaActual === "es" ? "en" : "es";
  setIdioma(nuevoIdioma);
  aplicarTraducciones();
}

// Inicializar cuando carga la página
document.addEventListener("DOMContentLoaded", function () {
  aplicarTraducciones();

  // Buscar el botón de idioma
  const botonIdioma = document.getElementById("boton-idioma");
  if (botonIdioma) {
    // Crear contenedor con popup
    const contenedor = document.createElement("div");
    contenedor.className = "contenedor-idioma";

    // Crear popup con botón de cerrar
    const popup = document.createElement("div");
    popup.className = "popup-idioma";
    popup.id = "popup-idioma";

    // Botón de cerrar
    const btnCerrar = document.createElement("button");
    btnCerrar.className = "popup-cerrar";
    btnCerrar.innerHTML = "×";
    btnCerrar.setAttribute("aria-label", "Cerrar");

    // Texto del popup
    const textoPopup = document.createElement("span");
    textoPopup.id = "texto-popup-idioma";

    // Actualizar texto del popup según idioma
    const actualizarPopup = () => {
      const idioma = getIdiomaActual();
      textoPopup.innerHTML =
        idioma === "es"
          ? "🌐 <strong>ENGLISH</strong> available"
          : "🌐 <strong>SPANISH</strong> available";
    };
    actualizarPopup();

    popup.appendChild(textoPopup);
    popup.appendChild(btnCerrar);

    // Insertar botón en el contenedor
    botonIdioma.parentNode.insertBefore(contenedor, botonIdioma);
    contenedor.appendChild(botonIdioma);
    contenedor.appendChild(popup);

    // Mostrar popup cada 3 visitas
    let visitasCount = parseInt(localStorage.getItem("visitasIdioma") || "0");
    visitasCount++;
    localStorage.setItem("visitasIdioma", visitasCount.toString());

    // Mostrar popup en la primera visita y luego cada 3 visitas
    if (visitasCount === 1 || visitasCount % 3 === 0) {
      popup.classList.add("mostrar");
    }

    // Cerrar popup al hacer clic en X
    btnCerrar.addEventListener("click", function (e) {
      e.stopPropagation();
      popup.classList.remove("mostrar");
      localStorage.setItem("popupIdiomaVisto", "true");
    });

    // Evento click en botón de idioma
    botonIdioma.addEventListener("click", function () {
      cambiarIdioma();
      actualizarPopup();
      popup.classList.remove("mostrar");
      localStorage.setItem("popupIdiomaVisto", "true");
    });
  }
});
